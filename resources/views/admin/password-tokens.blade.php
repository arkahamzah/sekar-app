<script>
async function cleanupExpiredTokens() {
    if (!confirm('Apakah Anda yakin ingin menghapus semua token yang telah kadaluarsa?')) {
        return;
    }

    try {
        const response = await fetch('{{ route("admin.password.cleanup") }}', {
            method: 'DELETE',
            headers: {
                'X-CSRF-TOKEN': '{{ csrf_token() }}',
                'Content-Type': 'application/json'
            }
        });

        const result = await response.json();

        if (result.success) {
            alert(result.message);
            window.location.reload();
        } else {
            alert('Terjadi kesalahan: ' + (result.message || 'Tidak dapat membersihkan token.'));
        }
    } catch (error) {
        console.error('Error:', error);
        alert('Terjadi kesalahan saat membersihkan token.');
    }
}

// Auto refresh every 30 seconds to update remaining time
setInterval(() => {
    window.location.reload();
}, 30000);
</script>
